package com.feriantes4dawin.feriavirtualmovil.ui.util;

/**
 * EnumMessageType 
 * 
 * Enumeración simple que representa estados de un mensaje 
 */
public enum EnumMessageType { 

    /**
     * Representa un mensaje de error. 
     */
    ERROR_MESSAGE,
    
    /**
     * Representa un mensaje de información o sugerencia. 
     */
    INFO_MESSAGE,
    
    /**
     * Representa un mensaje de advertencia o de atención. 
     */
    WARN_MESSAGE,

    /**
     * Indica un mensaje de pregunta o de ayuda. 
     */
    QUESTION_MESSAGE
}
